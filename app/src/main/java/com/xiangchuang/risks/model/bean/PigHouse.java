package com.xiangchuang.risks.model.bean;

public class PigHouse {
    public String name;
    public String id;

    public PigHouse(String name, String id) {
        this.name = name;
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }
}
